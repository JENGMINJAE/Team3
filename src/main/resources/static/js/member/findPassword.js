function findPw(){
    const memberId = document.querySelector('#member_id').value;
    const memberName = document.querySelector('#member_name').value;
    fetch('/member/findPwFetch', { //요청경로
        method: 'POST',
        cache: 'no-cache',
        headers: {
            'Content-Type': 'application/x-www-form-urlencoded; charset=UTF-8'
        },
        //컨트롤러로 전달할 데이터
        body: new URLSearchParams({
           // 데이터명 : 데이터값
           'memberId' : memberId,
           'memberName' : memberName
        })
    })
    .then((response) => {
        if(!response.ok){
            alert('fetch error!\n컨트롤러로 통신중에 오류가 발생했습니다.');
            return ;
        }
    
        //return response.text(); //컨트롤러에서 return하는 데이터가 없거나 int, String 일 때 사용
        return response.json(); //나머지 경우에 사용
    })
    //fetch 통신 후 실행 영역
    .then((data) => {//data -> controller에서 리턴되는 데이터!
        if(data){

        }
        else{
        document.querySelector("#failChecker").innerHTML = '';
            let str = '';
            str += `아이디와 비밀번호를 확인해주세요.`;
        document.querySelector("#failChecker").insertAdjacentHTML('beforeend',str);
        }
    })
    //fetch 통신 실패 시 실행 영역
    .catch(err=>{
        alert('fetch error!\nthen 구문에서 오류가 발생했습니다.\n콘솔창을 확인하세요!');
        console.log(err);
    });
}